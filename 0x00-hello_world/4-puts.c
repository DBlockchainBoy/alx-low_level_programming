/**
 * This program prints text on the
 * screen using puts
 */
#include <stdio.h>
#include <string.h>

/**
 * main: takes no argument and 
 * returns an integer.
 * char: takes character input
 * puts: prints input from char
 * return: returns 0
 */
int main(void)
{
	char demo_char[] = "\"Programming is like building a multilingual puzzle";
	puts(demo_char);
	return (0);
}
